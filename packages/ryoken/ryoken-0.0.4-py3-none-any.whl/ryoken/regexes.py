class Regexinator:
    def __init__(self):
        self._email = None
        self._username = None
        self._password = None

    async def validate(self, string: str, strategy: str = None) -> bool:
        if not strategy:
            raise TypeError('You must pass the keyword argument strategy')

        do_matching = self._get_strategy(strategy).fullmatch(string)

        return True if do_matching else False

    def _get_strategy(self, strategy: str):
        pattern, attr = self._get_pattern(strategy)

        if not getattr(self, attr):
            setattr(self, attr, compile(pattern))

        return getattr(self, attr)

    @staticmethod
    def _get_pattern(strategy: str):
        if strategy == 'USERNAME' or strategy == 'username':
            return USERNAME_FORMAT, '_username'
        elif strategy == 'PASSWORD' or strategy == 'password':
            return PASSWORD_STRENGTH, '_password'
        elif strategy == 'EMAIL' or strategy == 'email':
            return EMAIL_FORMAT, '_email'

        raise NotSupportedValidation
