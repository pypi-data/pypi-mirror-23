def doCT(comment):
    lines = ["", "", "", "", "", ""]
    for x in range(0,6):
        linestr=""
        for c in comment:
            linestr=linestr+FONTCT[c][x]
        lines[x]=linestr
    return lines

def dotitle(title):
    lines=doCT(title)
    output=''
    for l in lines:
        output=output+l+'\n'
    #output=output+'='*len(lines[0])+'\n'
    return output

